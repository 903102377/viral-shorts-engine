    fullScriptContext: string, 
    allCharactersContext: string, 
    actionHint: string, 
    dialogue: string,
    sceneIndex: number = 0,
    totalScenes: number = 1,
    previousImagePrompt: string = "",
    previousVideoPrompt: string = ""
